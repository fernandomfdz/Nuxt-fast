    await createOrganizationsMiddleware()
    
    // 6. Mostrar instrucciones finales
    await showFinalInstructions(selectedFeatures)
    
    readline.close()
    
    console.log('\n‚úÖ ¬°M√≥dulo de organizaciones a√±adido exitosamente!')
    console.log('\nüîÑ Por favor, reinicia el servidor para que los cambios surtan efecto:')
