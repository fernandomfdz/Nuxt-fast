async function updateBetterAuthConfig() {
  console.log('üîß Actualizando configuraci√≥n de Better Auth...')
  
  const authPath = join(process.cwd(), 'utils', 'auth.ts')
  
  if (!existsSync(authPath)) {
    console.warn('‚ö†Ô∏è  No se encontr√≥ utils/auth.ts, se crear√° autom√°ticamente')
    return
  }
  
  // Leer archivo actual
  let authContent = await fs.readFile(authPath, 'utf-8')
  
  // Verificar si ya incluye el plugin de organizaciones
  if (authContent.includes('organization')) {
    console.log('‚úÖ Plugin de organizaciones ya configurado en Better Auth')
    return
  }
  
  // A√±adir import del plugin de organizaciones
  if (!authContent.includes('import { organization }')) {
    authContent = authContent.replace(
      'import { mongodbAdapter }',
      `import { mongodbAdapter } from "better-auth/adapters/mongodb"
import { organization } from "better-auth/plugins"`
    )
  }
  
  // A√±adir plugin a la configuraci√≥n
  const pluginInsertion = `  plugins: [
    organization()
  ],`
  
  if (!authContent.includes('plugins:')) {
    authContent = authContent.replace(
      'trustedOrigins: [',
      `${pluginInsertion}
  trustedOrigins: [`
    )
  }
  
  await fs.writeFile(authPath, authContent, 'utf-8')
  
  console.log('‚úÖ Better Auth configurado con plugin de organizaciones')
}

